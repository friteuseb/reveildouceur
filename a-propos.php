      <div class="article__content">
        <p class="article__lead">
          Vous avez parfois l'impression que quelque chose ne colle pas ?
          Que les explications officielles sont trop simples, ou que certains sujets
          sont systématiquement évités ? Nous aussi.
        </p>

        <h2>Pourquoi ce site ?</h2>
        <p>
          Entre le conformisme aveugle et les théories farfelues, il existe un espace.
          Un espace où l'on peut poser des questions sans être taxé de "complotiste",
          et accepter la complexité sans se réfugier dans le déni.
        </p>
        <p>
          <strong>Réveil Douceur</strong>, c'est cet espace. Un lieu où la curiosité
          n'est pas un défaut, où le doute méthodique est une vertu, et où les faits
          priment sur les opinions.
        </p>

        <h2>Nos principes</h2>

        <h3>1. Des questions, pas des accusations</h3>
        <p>
          Nous ne vous dirons jamais "réveillez-vous" ou "on vous ment".
          Ces formules ferment le débat au lieu de l'ouvrir. Nous préférons poser des questions,
          présenter des données, et vous laisser tirer vos propres conclusions.
        </p>

        <h3>2. Toujours sourcé</h3>
        <p>
          Chaque article cite ses sources : études scientifiques, rapports officiels,
          données publiques, articles de presse. Pas de "on dit que" ni de "il paraît".
          Vous pouvez vérifier par vous-même.
        </p>

        <h3>3. Nuance et complexité</h3>
        <p>
          La réalité est rarement binaire. Le monde n'est pas divisé entre "gentils" et "méchants",
          entre "vérité officielle" et "vérité cachée". Nous explorons les zones grises,
          les paradoxes, les "oui, mais" que personne n'ose aborder.
        </p>

        <h3>4. Ni alarmisme, ni complotisme</h3>
        <p>
          Nous ne cherchons pas à vous faire peur, ni à désigner des coupables.
          Nous cherchons à comprendre. Les explications simples sont souvent fausses,
          mais les explications compliquées ne sont pas forcément vraies.
        </p>

        <h2>Ce que nous ne sommes pas</h2>
        <ul>
          <li>
            <strong>Un site de "vérité alternative"</strong> : nous nous appuyons sur les mêmes
            données que tout le monde, simplement nous posons des questions différentes.
          </li>
          <li>
            <strong>Un site militant</strong> : nous n'avons pas d'agenda politique.
            Les faits sont têtus, quelle que soit leur couleur politique.
          </li>
          <li>
            <strong>Un site de conseils</strong> : nous présentons des informations,
            pas des recommandations. Consultez des professionnels pour vos décisions importantes.
          </li>
        </ul>

        <h2>Notre méthode</h2>
        <p>
          Pour chaque sujet, nous suivons une démarche simple :
        </p>
        <ol>
          <li>Identifier une question qui mérite d'être posée</li>
          <li>Rassembler les données disponibles (études, statistiques, rapports)</li>
          <li>Présenter les faits de manière claire et accessible</li>
          <li>Explorer les différentes interprétations possibles</li>
          <li>Laisser le lecteur se forger sa propre opinion</li>
        </ol>

        <h2>Une invitation</h2>
        <p>
          Ce site est une invitation à la réflexion, pas une injonction à penser comme nous.
          Vous pouvez lire nos articles et ne pas être d'accord. Vous pouvez trouver nos questions
          mal posées ou nos analyses incomplètes. C'est normal, et c'est sain.
        </p>
        <p>
          L'important, c'est de continuer à poser des questions. De ne jamais cesser
          d'être curieux. De refuser les réponses toutes faites, qu'elles viennent
          des institutions ou des contre-pouvoirs.
        </p>
        <p class="article__highlight">
          <em>Questionnez. Vérifiez. Pensez par vous-même.</em>
        </p>
